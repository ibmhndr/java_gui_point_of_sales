    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      int ambil = this.jTable1.getSelectedRow();//untuk mengambil digit yang diseleksi
      if(ambil == -1)
          return;
      
      //untuk mengambil nilai pada beberapa variabel sesuai baris seleksi
      String id = (String) tabel1.getValueAt(ambil, 0);
      String nam = (String) tabel1.getValueAt(ambil, 1);
      String jns= (String) tabel1.getValueAt(ambil, 2);
      String har = (String) tabel1.getValueAt(ambil, 3);
      String stk = (String) tabel1.getValueAt(ambil, 4);
      //untuk mengkonversi beberapa variabel menjadi integer
      id1 = Integer.parseInt(id);
      cek = (String) tabel1.getValueAt(ambil, 4);
    
      nama.setText(nam);
      jenis.setText(jns);
      harga.setText(har);
      stok.setText(stk);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nbarang = nama.getText();
        String jnis = jenis.getText();
        int har = Integer.parseInt(harga.getText());
        String stk = stok.getText();
        
        if(!cek.equals(stk))
         {
          JOptionPane.showMessageDialog(this, "Stok tak dapat diubah!","Error",JOptionPane.ERROR_MESSAGE);
         }
         else
         {
          try
          {
           Connection c = KoneksiDB.getkoneksi();
           Statement s = c.createStatement();
            
           String tes1 = "SELECT username from login where kondisi = 1";
           ResultSet r = s.executeQuery(tes1);
                
           while(r.next())
           {
            namanya = r.getString(1);
           }
            
           String bar= "UPDATE barang SET nama_barang='"+nbarang+"', jenis='"+jnis+"', harga='"+har+"', stok='"+stk+"' WHERE id_barang='"+id1+"'";
           s.executeUpdate(bar);
           String his= "INSERT INTO history_barang(id_barang,username,date,time,deskripsi) VALUES('"+id1+"', '"+namanya+"', now(),now(),'Update Barang, "+nbarang+"')";
           s.executeUpdate(his);
           JOptionPane.showMessageDialog(this, "data berhasil diubah");
          }         
           catch(SQLException e) 
           { 
            JOptionPane.showMessageDialog(this, e);
           }
           nama.setText("");
           jenis.setText("");
           harga.setText("");
           stok.setText("");
           loadData();
         }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String cr = cari.getText();        
        try
        {
            tabel1.getDataVector().removeAllElements();
            tabel1.fireTableDataChanged();
            Connection c = KoneksiDB.getkoneksi();
            Statement s = c.createStatement();
            String sql= "SELECT * FROM barang WHERE nama_barang LIKE'%"+cr+"%'";
            ResultSet h = s.executeQuery(sql);
            tabel1.setRowCount(0);
            while(h.next()){
            Object[] o = new Object[5];
            o[0] = h.getString("id_barang");
            o[1] = h.getString("nama_barang");
            o[2] = h.getString("jenis");
            o[3] = h.getString("harga");
            o[4] = h.getString("stok");
            tabel1.addRow(o);
            }
            nama.setText("");
            jenis.setText("");
            harga.setText("");
            stok.setText("");
            cari.setText("");
        }         
        catch(SQLException e) 
         { 
             JOptionPane.showMessageDialog(this, e);
         }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

