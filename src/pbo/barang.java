    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nbarang = nama.getText();
        String jnis = jenis.getText();
        int har = Integer.parseInt(harga.getText());
        int stk = Integer.parseInt(stok.getText());
        
        try
        {
            Connection c = KoneksiDB.getkoneksi();
            Statement s = c.createStatement();
            
            String tes1 = "SELECT username from login WHERE kondisi = 1";
            ResultSet r = s.executeQuery(tes1);
                
            while(r.next())
            {
                namanya = r.getString(1);
            }
            
            String id= "SELECT id_barang FROM barang";//menseleksi nilai id untuk barang baru
            ResultSet idb = s.executeQuery(id);
            while(idb.next())
            {
             barang = idb.getInt("id_barang");
             barbaru = barang + 1;
            }
            String bar= "INSERT INTO barang(nama_barang,jenis,harga,stok) VALUES('"+nbarang+"', '"+jnis+"', '"+har+"', '"+stk+"')";
            s.executeUpdate(bar);
            String his= "INSERT INTO history_barang(id_barang,username,date,time,deskripsi) VALUES('"+barbaru+"', '"+namanya+"', now(),now(),'Tambah Barang, "+nbarang+"')";
            s.executeUpdate(his);
            JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");
        }         
        catch(SQLException e) 
         { 
             JOptionPane.showMessageDialog(this, e);
         }
        nama.setText("");
        jenis.setText("");
        harga.setText("");
        stok.setText("");
        loadData();
    }//GEN-LAST:event_jButton2ActionPerformed

